const SYSTEM_PROMPT = `أنت مساعد ذكي للإجابة على الأسئلة بخصوص الأوراق المطلوبة عند التبصيم في سوريا.

معلومات مهمة:
1. الثبوتيات الشخصية: الهوية القديمة، إخراج القيد حديثاً (مع الرقم الوطني وصورة مختومة)، دفتر العائلة (للمتزوجين)
2. ثبوتيات الزواج: دفتر عائلة قديم أو إنقاذ، بيان عائلي مختوم. عقد المحكمة والشيخ غير مقبولين
3. الشهادات العلمية: أعلى شهادة حصل عليها الأخ، أصلية أو مصدقة. بدون شهادة يُكتب "ابتدائي". الأجنبية تُترجم وتُصدق
4. شهادات الشمال السوري: تتطلب تصديق من وزارة التعليم العالي (عدا إدلب وحلب الحرة وحلب الشهباء). تُقبل 6 أشهر
5. المهاجرون: يستخرجون ثبوتياتهم من الشعب المدنية في مناطق تواجدهم
6. ملاحظات: هويات المجالس غير مقبولة، صور الجوال غير مقبولة، يجب الوضوح والتصديق
7. حالات خاصة (مكتومون/متوفون): ورقة تعريف من مختار الحي + ضبط المحكمة + دفتر عائلة الأهل

أجب بالعربية الفصحى. كن واضحاً ومفيداً ومختصراً. لا تذكر مصادر خارجية.`;

async function callAI(userMessage) {
    const settings = getSettings();
    if (!settings.apiKey) return null;

    try {
        const response = await fetch('https://api.groq.com/openai/v1/chat/completions', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'Authorization': 'Bearer ' + settings.apiKey
            },
            body: JSON.stringify({
                model: 'llama-3.1-8b-instant',
                messages: [
                    { role: 'system', content: SYSTEM_PROMPT },
                    { role: 'user', content: userMessage }
                ],
                temperature: 0.7,
                max_tokens: 1024
            })
        });

        if (!response.ok) {
            console.error('API Error:', response.status);
            return null;
        }

        const data = await response.json();
        return data.choices[0].message.content;
    } catch (e) {
        console.error('API Call Failed:', e);
        return null;
    }
}

