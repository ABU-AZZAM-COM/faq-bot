// ===== محرك البحث الذكي =====
function findBestResponse(input) {
    const inputLower = input.toLowerCase().trim();
    const settings = getSettings();

    // التحقق من التحيات
    const greetingWords = ['مرحبا', 'السلام عليكم', 'اهلا', 'أهلا', 'السلام', 'صباح', 'مساء', 'هاي', 'هلا', 'hello', 'hi'];
    if (greetingWords.some(g => inputLower.includes(g))) {
        const responses = knowledgeBase.greeting.responses;
        return responses[Math.floor(Math.random() * responses.length)];
    }

    // التحقق من الشكر
    if (knowledgeBase.farewell.keywords.some(k => inputLower.includes(k))) {
        const responses = knowledgeBase.farewell.responses;
        return responses[Math.floor(Math.random() * responses.length)];
    }

    // البحث في الأسئلة الشائعة أولاً
    let bestFaqScore = 0;
    let bestFaqAnswer = null;

    for (const cat of knowledgeBase.categories) {
        if (!cat.faq) continue;
        for (const faq of cat.faq) {
            let score = 0;
            for (const keyword of faq.q) {
                if (inputLower.includes(keyword.toLowerCase())) {
                    score += 15;
                }
            }
            if (score > bestFaqScore) {
                bestFaqScore = score;
                bestFaqAnswer = faq.a[settings.personality] || faq.a.formal;
            }
        }
    }

    if (bestFaqScore > 10) return bestFaqAnswer;

    // البحث في الفئات
    let bestCatScore = 0;
    let bestCatContent = null;

    for (const cat of knowledgeBase.categories) {
        let score = 0;
        for (const keyword of cat.keywords) {
            if (inputLower.includes(keyword.toLowerCase())) {
                score += 10;
            }
        }
        if (score > bestCatScore) {
            bestCatScore = score;
            bestCatContent = cat.content[settings.personality] || cat.content.formal;
        }
    }

    if (bestCatScore > 5) return bestCatContent;

    // رد افتراضي ذكي
    return getSmartFallback(input, settings);
}

